package cn.magicdu.bookmark.pojo;

public class InfoList {
	private String userid;
	private String username;
	private String bookmarkid;
	private String bookmarkname;
	private String bookmarkurl;
	private String bookmarkdes;
	private int bookmarkstate;
	private String categoryid;
	private String categoryname;

	public String getUserid() {
		return userid;
	}

	public void setUserid(String userid) {
		this.userid = userid;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getBookmarkid() {
		return bookmarkid;
	}

	public void setBookmarkid(String bookmarkid) {
		this.bookmarkid = bookmarkid;
	}

	public String getBookmarkname() {
		return bookmarkname;
	}

	public void setBookmarkname(String bookmarkname) {
		this.bookmarkname = bookmarkname;
	}

	public String getBookmarkurl() {
		return bookmarkurl;
	}

	public void setBookmarkurl(String bookmarkurl) {
		this.bookmarkurl = bookmarkurl;
	}

	public String getBookmarkdes() {
		return bookmarkdes;
	}

	public void setBookmarkdes(String bookmarkdes) {
		this.bookmarkdes = bookmarkdes;
	}

	public int getBookmarkstate() {
		return bookmarkstate;
	}

	public void setBookmarkstate(int bookmarkstate) {
		this.bookmarkstate = bookmarkstate;
	}

	public String getCategoryid() {
		return categoryid;
	}

	public void setCategoryid(String categoryid) {
		this.categoryid = categoryid;
	}

	public String getCategoryname() {
		return categoryname;
	}

	public void setCategoryname(String categoryname) {
		this.categoryname = categoryname;
	}

}
